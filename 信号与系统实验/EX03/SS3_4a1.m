n = -20:100;
x1 = exp(1i*pi*n/4);
x2 = sin(pi*n/8+pi/16);
x3 = (9/10).^n;
x4 = n+1;
subplot(5,1,1);
stem(n,real(x1));
title('real[exp(j*pi*n/4)]');
subplot(5,1,2);
stem(n,imag(x1));
title('imag[exp(j*pi*n/4)]');
subplot(5,1,3);
stem(n,x2);
title('sin(pi*n/8+pi/16)');
subplot(5,1,4);
stem(n,x3);
title('(9/10).^n');
subplot(5,1,5);
stem(n,x4);
title('n+1');